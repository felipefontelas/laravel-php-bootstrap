                <h2 id="sintomas">Sintomas do câncer de mama</h2>
                <p>O câncer de mama inicialmente é assintomático. As formas mais fáceis de se descobrir logo no início são por exames como mamografia, ultrassom ou ressonância magnética. O exame de toque também é importante, ao notar qualquer alteração na mama, agende uma consulta médica.

                    Algumas alterações físicas das mamas podem ser indícios de câncer de mama. Quando há presença destes indícios, é possível que a doença esteja em um nível avançado.  Fique de olho em alguns sintomas:

                    Dor ou inversão do mamilo
                    Vermelhidão ou descamação do mamilo ou da pele do      seio
                    Aparecimento de nódulos (caroços) no seio ou na  axilas,  podendo apresentar dor ou não, serem duros e  irregulares ou macios e redondos
                    Presença de secreção pelo mamilo, sanguinolenta ou    não
                    Inchaço irregular em parte da mama, que pode ficar  quente e vermelha.
                    Irritação ou retração na pele ou aparecimento de rugosidade semelhante à casca de laranja
                    Nos casos mais adiantados, é possível aparecer ulceração na pele com odor desagradável
                    Um caroço na mama não significa necessariamente câncer. Grande parte dos nódulos mamários são cistos e adenomas benignos. As mamas se modificam naturalmente ao longo do ciclo menstrual, porém, ao notar as alterações e sintomas descritos acima, é essencial ser consultado rapidamente.</p>
                <h3 id="autoexame">Autoexame de mama</h3>
                <p>É muito importante que as mulheres, uma vez por mês, façam o autoexame da mama. O autoexame é uma medida importante para identificar nódulos de um possível câncer de mama. Esse procedimento é preventivo e deve ser realizado todos os meses, sempre após o período menstrual.

                    É essencial estar atenta a alterações como retração da pele ou do mamilo, inchaços, assimetria, avermelhamento, secreção com sangue e gânglios que surgem nas axilas, crescentes ou não.

                    O autoexame não deve ser o único método preventivo, já que o câncer de mama pode ser imperceptível ao toque. Por isso, é indicado visitar anualmente o ginecologista e/ou mastologista para fazer os exames necessários, como a  mamografia, ultrassonografia mamária e ressonância magnética.</p>
