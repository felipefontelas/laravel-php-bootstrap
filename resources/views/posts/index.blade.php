                <h2 class="blog-post-title" id="dicas">Dicas e Recomendações</h2>
                <p class="blog-post-meta">1 de Outubro de 2017 <a href="#"> Dr. Prauzio Tarela</a></p>

                <p>O câncer de mama ainda não tem prevenção. O diagnóstico precisa ser realizado o mais cedo possível, utilizando-se para isto todos os exames de imagens possíveis. É necessário que façam o autoexame das mamas uma vez ao mês.

                    E quando a mulher chegar aos 40 anos, a mamografia começa a ser um exame importante para a detecção da doença. É recomendado que seja feito pelo menos uma vez por ano.

                    O melhor modo de prevenção da doença é estar em dia com os exames, para que não ocorra a detecção tardia com a doença em estágio avançado.

                </p>

                <p>Todo o câncer de mama é tratável, porém nem todos são curáveis. É preciso identificar a fase em que o tumor foi diagnosticado, para encontrar os melhores procedimentos para tratar a paciente.

                    O tratamento curativo se aplica aos tumores que estão localizados na mama ou até mesmo com metástase axilar, mas que não atingiram outros órgãos. Sendo assim, o foco é na cura do paciente.

                    O médico utilizará para o tratamento oncológico a cirurgia, a quimioterapia, a hormonioterapia e a radioterapia. Todos são tratamentos utilizados para o restabelecimento da saúde do paciente.</p>
