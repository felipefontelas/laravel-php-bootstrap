                <h2 class="blog-post-title" id="comotratar">Como tratar</h2>
                <p class="blog-post-meta">1 de Outubro de 2017 <a href="#"> Dr. Vrauzio Darela</a></p>

                <p>Após a detecção da doença com os exames necessários para o diagnóstico do câncer de mama, é necessário iniciar os tratamentos. Há dois possíveis tratamentos para o câncer de mama, um clínico e um cirúrgico.

                    Os tratamentos cirúrgicos envolvem a retirada da mama, a mastectomia, ou parte dela. Os tratamentos clínicos envolvem vários tipos de medicamentos, como os quimioterápicos, os hormonais e também a radioterapia, que deve ser empregada logo após o tratamento cirúrgico.</p>
                <p>Após o tratamento é possível fazer a reconstrução da mama, com o próprio <a href="#">mastologista</a> ou cirurgião plástico, colocando implantes mamários, que naturalmente influenciarão na auto estima da mulher e consequentemente na parte psicológica feminina.</p>
